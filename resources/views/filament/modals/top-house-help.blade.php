        <h3 class="font-semibold text-blue-900 dark:text-blue-100 mb-2"> 功能说明</h3>
        <p class="text-blue-800 dark:text-blue-200">
            房源推广功能允许您将优质房源置顶显示在小程序首页，提高曝光率和成交率。
        </p>
    </div>

    <div class="space-y-2">
        <h3 class="font-semibold text-gray-900 dark:text-gray-100"> 操作流程</h3>
        <ol class="list-decimal list-inside space-y-1 text-gray-700 dark:text-gray-300 ml-2">
            <li>在"房源列表"中找到要推广的房源</li>
            <li>点击"编辑"按钮</li>
            <li>在顶部"房源推广"区域，开启"置顶显示"</li>
            <li>系统会自动设置置顶时间为当前时间</li>
            <li>保存后，房源将出现在本页面，并在小程序首页置顶显示</li>
        </ol>
    </div>

    <div class="space-y-2">
        <h3 class="font-semibold text-gray-900 dark:text-gray-100"> 排序规则</h3>
        <ul class="list-disc list-inside space-y-1 text-gray-700 dark:text-gray-300 ml-2">
            <li>置顶房源按"置顶时间"倒序排列</li>
            <li>置顶时间越晚，排序越靠前</li>
            <li>可以通过"调整时间"功能修改排序位置</li>
        </ul>
    </div>

    <div class="space-y-2">
        <h3 class="font-semibold text-gray-900 dark:text-gray-100"> 小程序展示</h3>
        <ul class="list-disc list-inside space-y-1 text-gray-700 dark:text-gray-300 ml-2">
            <li>置顶房源会显示"推荐"标签</li>
            <li>在首页优先展示</li>
            <li>用户筛选时：
                <ul class="list-circle list-inside ml-6 mt-1">
                    <li>符合筛选条件的置顶房源继续置顶显示</li>
                    <li>不符合筛选条件的置顶房源不显示</li>
                </ul>
            </li>
        </ul>
    </div>

    <div class="bg-yellow-50 dark:bg-yellow-900/20 p-4 rounded-lg border border-yellow-200 dark:border-yellow-800">
        <h3 class="font-semibold text-yellow-900 dark:text-yellow-100 mb-2"> 温馨提示</h3>
        <ul class="list-disc list-inside space-y-1 text-yellow-800 dark:text-yellow-200 ml-2">
            <li>只有已上架且审核通过的房源才会在小程序显示</li>
            <li>下架的房源虽然会保留在推广列表，但不会在小程序显示</li>
            <li>可以批量取消置顶，方便管理</li>
        </ul>
